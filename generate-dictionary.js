// Generate dictionary entries
const dictionary = [];
let entryId = 1;

for (const [category, words] of Object.entries(categories)) {
  for (const [korean, myanmar] of words) {
    dictionary.push({
      korean: korean,
      myanmar: myanmar,
      category: category
    });
    entryId++;
  }
}

// Remove duplicates
const uniqueDict = [];
const seen = new Set();
for (const entry of dictionary) {
  const key = `${entry.korean}|${entry.category}`;
  if (!seen.has(key)) {
    seen.add(key);
    uniqueDict.push(entry);
  }
}

